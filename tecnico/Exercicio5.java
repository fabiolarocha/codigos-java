
public class Exercicio5 {
	public static void main(String[] args) {
		int mes = 4;
		
		switch(mes) {
			case 1:
				System.out.println("Janeiro");
				break;
			case 2:
				System.out.println("Fevereiro");
				break;
			case 3:
				System.out.println("Marco");
				break;
			default:
				System.out.println("Valor invalido");
				break;
		}
	}
}
